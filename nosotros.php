            <h5>¿Por qué Stemprocell™?</h5>

            <p>Stemprocell™ es un tratamiento de vanguardia que utiliza sus propias Células Madre. El procedimiento es ambulatorio, no implica riesgos y su aplicación se realiza con los más altos estándares médicos.</p>

            <h5>¿En qué consiste el procedimiento?</h5>
            <p>El tejido adiposo y/o la médula ósea y/o la sangre periferica se extraen del paciente mediante una mini liposucción, una aspiración de medula osea o una extración de sangre periferica y las muestras se procesan ​​con el protocolo Stemprocell™ para obtener Células Madre. En ciertos casos, se extrae un muestra de sangre y se procesa para obtener plasma rico en plaquetas (PRP) y sucesivamente plasma rico en  factores de crecimiento.</p>

            <p>Después, literalmente, millones de Células Madre, se administran al cuerpo e inmediatamente comienzan a trabajar.</p>

